package inheritance;

public class InheritanceDocs {
	
	/*
	 * ------------------ Inheritance -------------------
	 *   # code re-usability
	 *   # process of accessing properties and methods of 
	 *     parent/super/base class to child/sub/derived class
	 *     is known as inheritance.
	 *     
	 *   # also know as IS-A relationship.
	 *   # Its a Generalization to Specialization process.
	 *   # We can inherit only one class at a time.
	 *   # Multiple inheritance not supported in java with class
	 *      but supports with interface.
	 *      
	 *   # 'extends' keyword is used in inheritance.
	 *   # we cant inherit private properties,methods and constructors.      
	 * 
	 * 
	 *    # syntax:
	 *    
	 *         child_class   extends  parent_class{
	 *         
	 *            //properties  : parent + child
	 *            //methods     : parent + child
	 *         }
	 */

}
